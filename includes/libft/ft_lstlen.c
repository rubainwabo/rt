#include "libft.h"

int		ft_lstlen(t_list *list)
{
	t_list		*tmp;
	size_t		i;

	i = 0;
	tmp = list;
	if (tmp)
	{
		while (tmp)
		{
			i++;
			tmp = tmp->next;
		}
	}
	return (i);
}
