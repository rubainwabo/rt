#include "../header/rtv1.h"

t_mat3		mat3_scale(t_mat3 a, double c)
{
	a.m[0][0] *= c;
	a.m[0][1] *= c;
	a.m[0][2] *= c;
	a.m[1][0] *= c;
	a.m[1][1] *= c;
	a.m[1][2] *= c;
	a.m[2][0] *= c;
	a.m[2][1] *= c;
	a.m[2][2] *= c;
	return (a);
}
